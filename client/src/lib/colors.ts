export function colorNameToHex(name: string): string {
  if (!name) return "#E5E7EB";
  const key = name.toLowerCase().trim();

  if (COLOR_MAP[key]) return COLOR_MAP[key];

  for (const [keyword, hex] of FALLBACK_KEYWORDS) {
    if (key.includes(keyword)) return hex;
  }

  if (key.includes("/")) {
    const parts = key.split("/");
    for (const part of parts) {
      const trimmed = part.trim();
      if (COLOR_MAP[trimmed]) return COLOR_MAP[trimmed];
      for (const [keyword, hex] of FALLBACK_KEYWORDS) {
        if (trimmed.includes(keyword)) return hex;
      }
    }
  }

  return "#E5E7EB";
}

export function isLightColor(hex: string): boolean {
  if (!hex || hex.startsWith("linear") || hex.startsWith("rgba")) return true;
  try {
    const r = parseInt(hex.slice(1, 3), 16);
    const g = parseInt(hex.slice(3, 5), 16);
    const b = parseInt(hex.slice(5, 7), 16);
    return (r * 299 + g * 587 + b * 114) / 1000 > 180;
  } catch {
    return true;
  }
}
